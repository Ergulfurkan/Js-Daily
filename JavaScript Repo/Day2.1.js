// Aritmetik Operatörler Nelerdir?

// +, -, *, /, %, ++, --, ** bizim aritmetik operatörlerimizdir. 

let a = 5;
let b = 10;
let c;

// Toplama aritmetik işlemi. + işareti ile istenilen verileri toplar.
c = a + b;
console.log(c);

// Çıkartma aritmetik işlemi. - işareti ile istenilen verileri çıkartır.
c = b - a;
console.log(c);

// Çarpma aritmetik işlemi. * işareti ile istenilen verileri çarpar.
c = a * b;
console.log(c);

// Bölme aritmetik işlemi. / işareti ile istenilen verileri birbirine böler.
c = b / a;
console.log(c);

//Mod alma işlemi. % işareti ile istenilen verilerin bölümlerinden kalanı verir.
c = b % a;
console.log(c);

// 1 er artırma aritmetik işlemi. ++ ile istenilen veriyi 1 arttırır.
b = b++;
console.log(b);

// 1 er azaltma aritmetik işlemi. -- ile istenilen veriyi 1 azaltır.
b = b--;
console.log(b);

//Üst alma işlemi. ** ile sağdaki veriyi soldakinin üstü olarak işler
c = a ** b // a^b demektir bu işlem.
console.log(c);
